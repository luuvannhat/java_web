package com.devpro.javaweb22.model;

public class ProductAdd {
	private String productName;
	private String category;
	private String brand;
	private String product_desc;
	private String price_mua;
	private String price_ban;
	private String quantity;
	private String image;
	private String type;
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getProduct_desc() {
		return product_desc;
	}
	public void setProduct_desc(String product_desc) {
		this.product_desc = product_desc;
	}
	public String getPrice_mua() {
		return price_mua;
	}
	public void setPrice_mua(String price_mua) {
		this.price_mua = price_mua;
	}
	public String getPrice_ban() {
		return price_ban;
	}
	public void setPrice_ban(String price_ban) {
		this.price_ban = price_ban;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	

}
